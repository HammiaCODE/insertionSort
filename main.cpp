#include <iostream>
#include <vector>

using namespace std;

void instertionSort(vector <int>& arr){
    int n = arr.size();

    for(int i = 1; i < n; i++){
        int key = arr[i];
        int j = i -1;

        while (j >= 0 && arr[j] > key){
            arr[j + 1] = arr[j];
            j = j -1;
        }

        arr[j + 1] = key;
    }
}

int main(){
    vector <int> arr = {5,3,4,8,1,3};

    instertionSort(arr);

    for(int num:arr){
        cout << num << "" << endl;
    }
    return 0;
}

